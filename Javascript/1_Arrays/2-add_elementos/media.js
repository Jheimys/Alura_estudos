// A função push() add um elemento na última posição do array.
const notas = [10, 9, 8]
notas.push(7)
console.log("Lista de notas:", notas)
console.log("Comprimento da lista de notas:", notas.length)

const notas2 = [1, 2, 3]
notas2.push()
console.log("Lista de notas2:", notas2)
console.log("Comprimento da lista de notas2:", notas2.length)
